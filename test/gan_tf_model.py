def TFGradOfDisc(dimension, interpolates):
    #differences = fake_data - real_data
    #interpolates = real_data + (alpha*differences)
    gradients = tf.gradients(GoodDiscriminator(interpolates, dimension), [interpolates])[0]
    slopes = tf.sqrt(tf.reduce_sum(tf.square(gradients), reduction_indices=[1]))
    gradient_penalty = tf.reduce_mean((slopes-1.)**2) * LAMBDA
    return gradient_penalty

